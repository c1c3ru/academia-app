import React, { createContext, useContext, useEffect, useState } from 'react';
import { 
  signInWithEmailAndPassword, 
  createUserWithEmailAndPassword, 
  signOut, 
  onAuthStateChanged,
  GoogleAuthProvider,
  FacebookAuthProvider,
  OAuthProvider,
  signInWithCredential,
  signInWithPopup
} from 'firebase/auth';
import { doc, getDoc, setDoc } from 'firebase/firestore';
import { auth, db } from '../services/firebase';

const AuthContext = createContext();

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (!context) {
    throw new Error('useAuth deve ser usado dentro de um AuthProvider');
  }
  return context;
};

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null);
  const [userProfile, setUserProfile] = useState(null);
  const [academia, setAcademia] = useState(null);
  const [loading, setLoading] = useState(true);

  const fetchAcademiaData = async (academiaId) => {
    try {
      console.log('üè¢ fetchAcademiaData: Buscando dados da academia:', academiaId);
      const academiaDoc = await getDoc(doc(db, 'academias', academiaId));
      if (academiaDoc.exists()) {
        console.log('‚úÖ fetchAcademiaData: Academia encontrada');
        setAcademia({
          id: academiaId,
          ...academiaDoc.data()
        });
      } else {
        console.log('‚ùå fetchAcademiaData: Academia n√£o encontrada');
        setAcademia(null);
      }
    } catch (error) {
      console.error('‚ùå fetchAcademiaData: Erro ao buscar dados da academia:', error);
    }
  };

  const fetchUserProfile = async (userId) => {
    try {
      console.log('üîç fetchUserProfile: Buscando perfil para userId:', userId);
      
      // Buscar na cole√ß√£o 'users'
      console.log('üîç fetchUserProfile: Buscando em users...');
      let userDoc = await getDoc(doc(db, 'users', userId));
      let foundIn = null;
      
      if (userDoc.exists()) {
        foundIn = 'users';
        console.log('‚úÖ fetchUserProfile: Encontrado em users');
      }
      
      if (userDoc.exists()) {
        const userData = userDoc.data();
        console.log('üìä fetchUserProfile: Dados do usu√°rio carregados:', {
          email: userData.email,
          academiaId: userData.academiaId,
          tipo: userData.tipo,
          userType: userData.userType,
          foundIn: foundIn,
          hasAcademiaId: !!userData.academiaId
        });
        
        setUserProfile(userData);
        
        // Se o usu√°rio tem academiaId, buscar dados da academia
        if (userData.academiaId) {
          console.log('üè¢ fetchUserProfile: Usu√°rio tem academiaId, buscando dados da academia...');
          await fetchAcademiaData(userData.academiaId);
        } else {
          console.log('‚ö†Ô∏è fetchUserProfile: Usu√°rio SEM academiaId - ser√° redirecionado para sele√ß√£o');
          setAcademia(null);
        }
      } else {
        console.log('‚ùå fetchUserProfile: Usu√°rio n√£o encontrado em nenhuma cole√ß√£o');
        setUserProfile(null);
        setAcademia(null);
      }
    } catch (error) {
      console.error('‚ùå fetchUserProfile: Erro ao buscar perfil do usu√°rio:', error);
      console.error('‚ùå fetchUserProfile: Detalhes:', {
        userId,
        code: error.code,
        message: error.message
      });
    }
  };

  useEffect(() => {
    const unsubscribe = onAuthStateChanged(auth, async (firebaseUser) => {
      console.log('üîê AuthStateChanged: Firebase user mudou:', firebaseUser?.email || 'null');
      
      if (firebaseUser) {
        console.log('üîê AuthStateChanged: Usu√°rio logado, definindo user state');
        setUser(firebaseUser);
        
        // TESTE: Verificar se fetchUserProfile existe
        console.log('üîê AuthStateChanged: fetchUserProfile existe?', typeof fetchUserProfile);
        
        // Buscar perfil do usu√°rio no Firestore
        console.log('üîê AuthStateChanged: Chamando fetchUserProfile para UID:', firebaseUser.uid);
        
        // Chamada direta com log imediato
        console.log('üîê AuthStateChanged: ANTES de chamar fetchUserProfile');
        try {
          await fetchUserProfile(firebaseUser.uid);
          console.log('üîê AuthStateChanged: fetchUserProfile conclu√≠do');
        } catch (error) {
          console.error('üîê AuthStateChanged: Erro no fetchUserProfile:', error);
        }
        console.log('üîê AuthStateChanged: DEPOIS de chamar fetchUserProfile');
      } else {
        console.log('üîê AuthContext: Usu√°rio deslogado, limpando estados');
        setUser(null);
        setUserProfile(null);
        setAcademia(null);
      }
      setLoading(false);
    });

    return unsubscribe;
  }, []);

  const signUp = async (email, password, userData) => {
    try {
      const { user: firebaseUser } = await createUserWithEmailAndPassword(auth, email, password);
      
      // Criar perfil do usu√°rio na cole√ß√£o 'users'
      await setDoc(doc(db, 'users', firebaseUser.uid), {
        ...userData,
        email,
        tipo: userData.tipo || 'aluno', // Padr√£o para aluno
        createdAt: new Date(),
        updatedAt: new Date()
      });

      await fetchUserProfile(firebaseUser.uid);
      return firebaseUser;
    } catch (error) {
      throw error;
    }
  };

  const signIn = async (email, password) => {
    try {
      console.log('üîê Tentando login com:', { 
        email: email, 
        emailType: typeof email,
        emailLength: email ? email.length : 0,
        password: password ? '***' : 'undefined',
        passwordType: typeof password,
        passwordLength: password ? password.length : 0
      });
      console.log('üìß Email v√°lido:', email && email.includes('@'));
      console.log('üìß Email trim:', email ? email.trim() : 'undefined');
      console.log('üîë Senha trim:', password ? password.trim() : 'undefined');
      
      // Limpar e validar dados
      const cleanEmail = email ? email.trim().toLowerCase() : '';
      const cleanPassword = password ? password.trim() : '';
      
      console.log('üßπ Dados limpos:', {
        email: cleanEmail,
        password: cleanPassword ? '***' : 'undefined'
      });
      
      if (!cleanEmail || !cleanPassword) {
        throw new Error('Email e senha s√£o obrigat√≥rios');
      }
      
      const { user: firebaseUser } = await signInWithEmailAndPassword(auth, cleanEmail, cleanPassword);
      console.log('‚úÖ Login bem-sucedido:', firebaseUser.email);
      
      await fetchUserProfile(firebaseUser.uid);
      return firebaseUser;
    } catch (error) {
      console.error('‚ùå Erro detalhado no login:', {
        code: error.code,
        message: error.message,
        email: email,
        emailType: typeof email,
        passwordLength: password ? password.length : 0,
        passwordType: typeof password,
        stack: error.stack
      });
      throw error;
    }
  };

  const signInWithGoogle = async () => {
    try {
      const provider = new GoogleAuthProvider();
      provider.addScope('profile');
      provider.addScope('email');
      
      const result = await signInWithPopup(auth, provider);
      const firebaseUser = result.user;
      
      // Verificar se o usu√°rio j√° existe no Firestore
      let userDoc = await getDoc(doc(db, 'users', firebaseUser.uid));
      
      if (!userDoc.exists()) {
        // Criar perfil b√°sico para usu√°rio do Google
        await setDoc(doc(db, 'users', firebaseUser.uid), {
          name: firebaseUser.displayName,
          email: firebaseUser.email,
          photoURL: firebaseUser.photoURL,
          tipo: null, // Ser√° definido na tela de sele√ß√£o
          userType: null, // Ser√° definido na tela de sele√ß√£o
          profileCompleted: false, // Indica que precisa completar o perfil
          isActive: true,
          createdAt: new Date(),
          updatedAt: new Date()
        });
      }
      
      await fetchUserProfile(firebaseUser.uid);
      return firebaseUser;
    } catch (error) {
      console.error('Erro no login Google:', error);
      throw error;
    }
  };

  const signInWithFacebook = async () => {
    try {
      const provider = new FacebookAuthProvider();
      provider.addScope('email');
      provider.addScope('public_profile');
      
      const result = await signInWithPopup(auth, provider);
      const firebaseUser = result.user;
      
      // Verificar se o usu√°rio j√° existe no Firestore
      let userDoc = await getDoc(doc(db, 'users', firebaseUser.uid));
      
      if (!userDoc.exists()) {
        await setDoc(doc(db, 'users', firebaseUser.uid), {
          name: firebaseUser.displayName,
          email: firebaseUser.email,
          photoURL: firebaseUser.photoURL,
          tipo: null, // Ser√° definido na tela de sele√ß√£o
          userType: null, // Ser√° definido na tela de sele√ß√£o
          profileCompleted: false, // Indica que precisa completar o perfil
          isActive: true,
          createdAt: new Date(),
          updatedAt: new Date()
        });
      }
      
      await fetchUserProfile(firebaseUser.uid);
      return firebaseUser;
    } catch (error) {
      console.error('Erro no login Facebook:', error);
      throw error;
    }
  };

  const signInWithMicrosoft = async () => {
    try {
      const provider = new OAuthProvider('microsoft.com');
      provider.addScope('email');
      provider.addScope('profile');
      
      const result = await signInWithPopup(auth, provider);
      const firebaseUser = result.user;
      
      let userDoc = await getDoc(doc(db, 'users', firebaseUser.uid));
      
      if (!userDoc.exists()) {
        userDoc = await getDoc(doc(db, 'users', firebaseUser.uid));
      }
      
      if (!userDoc.exists()) {
        await setDoc(doc(db, 'users', firebaseUser.uid), {
          name: firebaseUser.displayName,
          email: firebaseUser.email,
          photoURL: firebaseUser.photoURL,
          tipo: null, // Ser√° definido na tela de sele√ß√£o
          userType: null, // Ser√° definido na tela de sele√ß√£o
          profileCompleted: false, // Indica que precisa completar o perfil
          isActive: true,
          createdAt: new Date(),
          updatedAt: new Date()
        });
      }
      
      await fetchUserProfile(firebaseUser.uid);
      return firebaseUser;
    } catch (error) {
      console.error('Erro no login Microsoft:', error);
      throw error;
    }
  };

  const signInWithApple = async () => {
    try {
      const provider = new OAuthProvider('apple.com');
      provider.addScope('email');
      provider.addScope('name');
      
      const result = await signInWithPopup(auth, provider);
      const firebaseUser = result.user;
      
      let userDoc = await getDoc(doc(db, 'users', firebaseUser.uid));
      
      if (!userDoc.exists()) {
        userDoc = await getDoc(doc(db, 'users', firebaseUser.uid));
      }
      
      if (!userDoc.exists()) {
        await setDoc(doc(db, 'users', firebaseUser.uid), {
          name: firebaseUser.displayName || 'Usu√°rio Apple',
          email: firebaseUser.email,
          photoURL: firebaseUser.photoURL,
          tipo: null, // Ser√° definido na tela de sele√ß√£o
          userType: null, // Ser√° definido na tela de sele√ß√£o
          profileCompleted: false, // Indica que precisa completar o perfil
          isActive: true,
          createdAt: new Date(),
          updatedAt: new Date()
        });
      }
      
      await fetchUserProfile(firebaseUser.uid);
      return firebaseUser;
    } catch (error) {
      console.error('Erro no login Apple:', error);
      throw error;
    }
  };

  const logout = async () => {
    try {
      console.log('üîê AuthContext: Iniciando signOut do Firebase...');
      console.log('üîê AuthContext: User atual antes do logout:', user?.email);
      console.log('üîê AuthContext: Auth object:', auth);
      
      await signOut(auth);
      console.log('üîê AuthContext: SignOut executado com sucesso');
      
      console.log('üîê AuthContext: Limpando estados locais...');
      setUser(null);
      setUserProfile(null);
      setAcademia(null);
      
      console.log('üîê AuthContext: Logout completo - estados limpos');
    } catch (error) {
      console.error('üîê AuthContext: Erro no signOut:', error);
      console.error('üîê AuthContext: Detalhes do erro:', {
        code: error.code,
        message: error.message,
        stack: error.stack
      });
      throw error;
    }
  };

  const updateUserProfile = async (updates) => {
    try {
      console.log('üìù updateUserProfile: Iniciando atualiza√ß√£o do perfil');
      console.log('üìù updateUserProfile: Updates:', updates);
      console.log('üìù updateUserProfile: User UID:', user?.uid);
      console.log('üìù updateUserProfile: UserProfile atual:', {
        email: userProfile?.email,
        academiaId: userProfile?.academiaId,
        tipo: userProfile?.tipo
      });
      
      if (user) {
        const updateData = {
          ...userProfile,
          ...updates,
          updatedAt: new Date()
        };
        
        console.log('üìù updateUserProfile: Dados finais para salvar:', {
          email: updateData.email,
          academiaId: updateData.academiaId,
          tipo: updateData.tipo,
          updatedAt: updateData.updatedAt
        });
        
        // Atualizar na cole√ß√£o 'users'
        console.log('üìù updateUserProfile: Salvando em users...');
        await setDoc(doc(db, 'users', user.uid), updateData, { merge: true });
        console.log('‚úÖ updateUserProfile: Salvo com sucesso em users');
        
        console.log('üìù updateUserProfile: Recarregando perfil...');
        await fetchUserProfile(user.uid);
        console.log('‚úÖ updateUserProfile: Perfil recarregado');
      } else {
        console.error('‚ùå updateUserProfile: Usu√°rio n√£o est√° logado');
      }
    } catch (error) {
      console.error('‚ùå updateUserProfile: Erro na atualiza√ß√£o:', error);
      console.error('‚ùå updateUserProfile: Detalhes:', {
        userId: user?.uid,
        updates,
        code: error.code,
        message: error.message
      });
      throw error;
    }
  };

  const updateAcademiaAssociation = async (academiaId) => {
    try {
      console.log('üîó updateAcademiaAssociation: Iniciando associa√ß√£o com academia:', academiaId);
      console.log('üîó updateAcademiaAssociation: User UID:', user?.uid);
      console.log('üîó updateAcademiaAssociation: User email:', user?.email);
      
      if (user) {
        console.log('üîó updateAcademiaAssociation: Atualizando perfil do usu√°rio...');
        await updateUserProfile({ academiaId });
        console.log('‚úÖ updateAcademiaAssociation: Perfil atualizado com sucesso');
        
        console.log('üîó updateAcademiaAssociation: Buscando dados da academia...');
        await fetchAcademiaData(academiaId);
        console.log('‚úÖ updateAcademiaAssociation: Associa√ß√£o completa!');
      } else {
        console.error('‚ùå updateAcademiaAssociation: Usu√°rio n√£o est√° logado');
      }
    } catch (error) {
      console.error('‚ùå updateAcademiaAssociation: Erro na associa√ß√£o:', error);
      console.error('‚ùå updateAcademiaAssociation: Detalhes:', {
        academiaId,
        userId: user?.uid,
        code: error.code,
        message: error.message
      });
      throw error;
    }
  };

  const value = {
    user,
    userProfile,
    academia,
    loading,
    signUp,
    signIn,
    signInWithGoogle,
    signInWithFacebook,
    signInWithMicrosoft,
    signInWithApple,
    logout,
    updateUserProfile,
    updateAcademiaAssociation,
    fetchUserProfile,
    fetchAcademiaData
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
};

